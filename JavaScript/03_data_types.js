// Primitive

// String
const value = "prash"
console.log(typeof value, value);

// Number
const num = 10
console.log(typeof num, num);

// boolean
const isStudying = true
console.log(typeof isStudying, isStudying);

// undefined
let hello = undefined;
console.log(typeof hello, hello);

// Null
let isEmpty = null
console.log(typeof isEmpty, isEmpty);








